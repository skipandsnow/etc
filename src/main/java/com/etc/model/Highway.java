package com.etc.model;

public class Highway {
	public String etc_date;
	public String highwayid;
	public String highwayname;
	public double meanspeed;
	public int caramount;

	public String getEtc_date() {
		return etc_date;
	}

	public void setEtc_date(String etc_date) {
		this.etc_date = etc_date;
	}

	public String getHighwayid() {
		return highwayid;
	}

	public void setHighwayid(String highwayid) {
		this.highwayid = highwayid;
	}

	public String getHighwayname() {
		return highwayname;
	}

	public void setHighwayname(String highwayname) {
		this.highwayname = highwayname;
	}

	public double getMeanspeed() {
		return meanspeed;
	}

	public void setMeanspeed(double meanspeed) {
		this.meanspeed = meanspeed;
	}

	public int getCaramount() {
		return caramount;
	}

	public void setCaramount(int caramount) {
		this.caramount = caramount;
	}

}
